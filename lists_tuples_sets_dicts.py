# Lists, Tuples, Sets, Dictionaries Examples

# List
fruits = ["apple", "banana", "cherry"]
print("List:", fruits)
fruits.append("orange")
print("After append:", fruits)

# Tuple
point = (10, 20)
print("Tuple:", point)
print("First element:", point[0])

# Set
unique_numbers = {1, 2, 3, 2, 1}
print("Set:", unique_numbers)
unique_numbers.add(4)
print("After add:", unique_numbers)

# Dictionary
details = {"name": "Alice", "age": 25}
print("Dictionary:", details)
print("Name:", details["name"])
details["city"] = "New York"
print("After adding city:", details)
