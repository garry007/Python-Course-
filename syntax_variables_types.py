# Python Syntax, Variables, and Data Types Examples

# Syntax: print statement
print("Hello, Python!")

# Variables
x = 5
name = "Alice"

# Data Types
integer_var = 10           # int
float_var = 3.14           # float
string_var = "Python"      # str
bool_var = True            # bool

print("Integer:", integer_var)
print("Float:", float_var)
print("String:", string_var)
print("Boolean:", bool_var)

# Type checking
print("Type of x:", type(x))
print("Type of name:", type(name))
